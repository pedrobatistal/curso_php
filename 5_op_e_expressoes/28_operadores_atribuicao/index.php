<?php

// +=
$a = 0;
$a += 10; // $a = $a + 10
echo $a . "<br>";

//-=
$b = 10;
$b -= 5; // $b = $b - 5
echo $b . "<br>";

//*=
$c = 15;
$c *= 2; // $c = $c * 2
echo $c . "<br>";

// /= 
$d = 20;
$d /= 4; // $d = $d / 4
echo $d . "<br>";

// %=
$e = 30;
$e %= 7; // $e = $e % 2
echo $e . "<br>";

//podemos utilizar variáveis também!!!

$f = 5;
$k = 9;

$f += $k;
echo $f . "<br>";